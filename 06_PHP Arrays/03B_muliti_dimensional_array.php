echo $matrix[1][1]."\n"; //2nd array 2nd item
echo $matrix[1][2]."\n"; //2nd array 3nd item
echo $matrix[2][0]."\n"; //3rd array first item 
echo $matrix[2][1]."\n"; //3rd array 2nd item
