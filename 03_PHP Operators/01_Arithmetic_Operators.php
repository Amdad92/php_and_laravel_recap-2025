<?php 
// Define two numbers
$x = 15;
$y = 5;

// Addition
echo "Addition: " . ($x + $y) . "\n";

// Subtraction
echo "Subtraction: " . ($x - $y) . "\n";

// Multiplication
echo "Multiplication: " . ($x * $y) . "\n";

// Division
echo "Division: " . ($x / $y) . "\n";

// Exponentiation
echo "Exponentiation: " . ($x ** $y) . "\n"; // $x raised to the power of $y

// Modulus
echo "Modulus: " . ($x % $y) . "\n";

?>